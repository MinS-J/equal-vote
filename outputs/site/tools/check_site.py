def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def check_local_refs() -> None:
    html = read_text(ROOT / "index.html") + "\n" + read_text(ROOT / "docs.html")
    refs = re.findall(r'(?:src|href)="(\./[^"#]+)"', html)
    missing = []
    for ref in refs:
        target = ROOT / urlsplit(ref).path.removeprefix("./")
        if not target.exists():
            missing.append(ref)
    if missing:
        raise AssertionError("Missing local references: " + ", ".join(missing))


def check_no_local_paths() -> None:
    files = [
        ROOT / "index.html",
        ROOT / "docs.html",
        ROOT / "styles.css",
        ROOT / "app.js",
        ROOT / "docs.js",
        ROOT / "assets" / "data" / "site-data.js",
        ROOT / "README_DEPLOY.md",
        ROOT / "VERCEL_DASHBOARD_DEPLOY.md",
        ROOT / "PUBLICATION_STATUS.md",
        ROOT / "DEPLOYMENT_REQUEST.md",
    ]
    bad_patterns = ["C:\\Users", "../work", "TODO", "FIXME", "undefined"]
    hits = []
    for path in files:
        text = read_text(path)
        for pattern in bad_patterns:
            if pattern in text:
                hits.append(f"{path.name}: {pattern}")
    if hits:
        raise AssertionError("Unexpected deploy text found: " + ", ".join(hits))


