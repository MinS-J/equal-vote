def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def check_integrity_manifest() -> None:
    manifest_path = ROOT / "assets" / "data" / "integrity-manifest.json"
    manifest = json.loads(read_text(manifest_path))
    if manifest.get("algorithm") != "sha256":
        raise AssertionError("integrity manifest algorithm must be sha256")

    failures = []
    for entry in manifest.get("files", []):
        path = ROOT / entry["path"]
        if not path.exists():
            failures.append(f"missing {entry['path']}")
            continue
        size = path.stat().st_size
        digest = sha256(path)
        if size != entry["bytes"] or digest != entry["sha256"]:
            failures.append(f"mismatch {entry['path']}")

    if failures:
        raise AssertionError("integrity manifest failures: " + ", ".join(failures))


def check_http_serving() -> None:
    class QuietHandler(SimpleHTTPRequestHandler):
        def log_message(self, format: str, *args) -> None:
            return

    server = ThreadingHTTPServer(("127.0.0.1", 0), QuietHandler)
    port = server.server_address[1]
    thread = Thread(target=server.serve_forever, daemon=True)

    cwd = Path.cwd()
    try:
        import os

        os.chdir(ROOT)
        thread.start()
        for path in REQUIRED_HTTP_PATHS:
            with urlopen(f"http://127.0.0.1:{port}{path}", timeout=5) as response:
                body = response.read(64)
                if response.status != 200 or not body:
                    raise AssertionError(
                        f"{path} failed: status={response.status}, bytes={len(body)}"
                    )
    finally:
        server.shutdown()
        server.server_close()
        import os

        os.chdir(cwd)


